package com.itheima.principles.demo2.after;

/**
 * @Description: 正方形
 */
public class Square implements Quadrilateral {

    // 边长
    private double side;

    public double getSide() {
        return side;
    }

    public void setSide(double side) {
        this.side = side;
    }

    public double getLength() {
        return side;
    }

    public double getWidth() {
        return side;
    }
}
